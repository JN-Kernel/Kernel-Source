package com.aode.dto;

public class Meau {

	private Integer meauId;
	
	private String meauname;
	
	private String href;



	public Integer getMeauId() {
		return meauId;
	}

	public void setMeauId(Integer meauId) {
		this.meauId = meauId;
	}

	public String getMeauname() {
		return meauname;
	}

	public void setMeauname(String meauname) {
		this.meauname = meauname;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}
	
	
}
